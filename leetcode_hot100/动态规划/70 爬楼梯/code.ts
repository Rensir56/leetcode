function climbStairs(n: number): number {
    if (n===1)
        return 1;
    
    const dp:number[] = new Array(n+1);
    dp[0] = 1;
    dp[1] = 1;
    for(let i:number=2;i<=n;i++){
        dp[i] = dp[i-1]+dp[i-2];
    }
    return dp[n];
};